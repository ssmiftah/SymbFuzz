"""
drivers/verilator.py — Verilator backend.

Spawns the Verilator-compiled simulation executable and communicates via the
shared JSON-over-stdio protocol (see drivers/base.py). The C++ harness is
generated by :func:`symfuzz.testbench_gen.generate_verilator_harness` and
compiled by the generated ``compile_<module>.sh`` script.
"""
from __future__ import annotations

import re
import shutil
import subprocess
from pathlib import Path
from typing import Optional

from .base import BaseStdioDriver, CoverageSnapshot


class VerilatorDriver(BaseStdioDriver):
    backend_name = "verilator"

    def collect_coverage(self) -> Optional[CoverageSnapshot]:
        resp = self._send({"cmd": "coverage"})
        if "error" in resp:
            if self.verbose:
                print(f"[verilator] coverage error: {resp['error']}")
            return None
        report = resp.get("report", "")
        if not report:
            return None
        top = getattr(self.design, "module_name", None)
        return _parse_verilator_cov_dat(report, top_module=top)

    @staticmethod
    def _check_installed() -> str:
        """Return the verilator binary path or raise a clear error."""
        path = shutil.which("verilator")
        if path is None:
            raise RuntimeError(
                "verilator not found on PATH. "
                "Install Verilator ≥ 5.0 (e.g. 'sudo apt install verilator')."
            )
        return path

    @property
    def _vtb_dir(self) -> Path:
        return self.tb_dir / "verilator"

    @property
    def _executable(self) -> Path:
        return self._vtb_dir / "obj_dir" / f"{self.design.module_name}_sim"

    def compile(self) -> None:
        self._check_installed()
        script = self._vtb_dir / f"compile_{self.design.module_name}.sh"
        if not script.exists():
            raise RuntimeError(
                f"verilator compile script not found: {script}. "
                f"Did generate_verilator_harness run?"
            )
        if self.verbose:
            print("[symfuzz] Compiling with Verilator ...")
        result = subprocess.run(
            ["bash", str(script)],
            cwd=str(self._vtb_dir),
            capture_output=True, text=True,
        )
        if result.returncode != 0:
            raise RuntimeError(
                f"verilator compile failed:\n{result.stdout}\n{result.stderr}"
            )
        if self.verbose:
            print(result.stdout.strip())

    # ------------------------------------------------------------------ #
    # Subprocess lifecycle                                                 #
    # ------------------------------------------------------------------ #

    def load(self) -> None:
        exe = self._executable
        if not exe.exists():
            raise RuntimeError(
                f"Verilator snapshot not found: {exe}. Call compile() first."
            )
        if self.verbose:
            print(f"[symfuzz] Starting verilator {exe.name} ...")
        self._proc = subprocess.Popen(
            [str(exe)],
            stdin=subprocess.PIPE,
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
            text=True, bufsize=1,
            cwd=str(self._vtb_dir),
        )


# ---------------------------------------------------------------------- #
# Verilator cov.dat parser                                                 #
# ---------------------------------------------------------------------- #

# Verilator 5.x coverage dump lines look like:
#   C '\x01f\x02<file>\x01l\x02<line>\x01n\x02<col>\x01page\x02<page>\x01o\x02<obj>\x01h\x02<hier>' <count>
#
# Each entry is a sequence of \x01<key>\x02<value> segments. Keys can be
# multi-character (e.g. "page"). The prior parser matched single-char keys
# only and captured the wrong values. Correct form: key = non-\x02 run
# following \x01, value = non-\x01 run following \x02.
#
# Known keys:
#   f    - source file path
#   l    - line number (start)
#   n    - column number
#   page - coverage category: v_branch, v_toggle, v_line, v_user
#   o    - object: "if"/"else"/"elsif"/"block" (branch/line),
#          "<signal>" or "<signal>[<bit>]" (toggle), cover-property name (user)
#   S    - optional extra start line
#   h    - hierarchy (e.g. TOP.counter)

_FIELD_RE = re.compile(r"\x01([^\x02]+)\x02([^\x01]*)")
_COV_LINE_RE = re.compile(r"^C '([^']*)'\s+(\d+)\s*$", re.MULTILINE)


_OBJ_BIT_RE = re.compile(r"^(\w[\w$.]*?)\[(\d+)\]$")


def _fully_qualified(hier: str, sig: str, top_module: str | None) -> str:
    """Build a dotted arch_name matching Yosys's flattened register names.

    Verilator's `hier` field starts with "TOP" and includes the top-module
    instance level, e.g. "TOP.serdiv.gen_lzc". Yosys's flattened
    `register.arch_name` is either bare ("state_q") for top-level flops or
    dotted ("u_inst.count") for submodule flops — with the top module name
    stripped.
    """
    parts = hier.split(".") if hier else []
    while parts and (parts[0] == "TOP" or parts[0] == top_module):
        parts.pop(0)
    return ".".join(parts + [sig]) if parts else sig


def _parse_verilator_cov_dat(text: str,
                             top_module: str | None = None) -> CoverageSnapshot:
    snap = CoverageSnapshot()
    total = 0
    for m in _COV_LINE_RE.finditer(text):
        fields_blob = m.group(1)
        hits = int(m.group(2))
        fields = {k: v for k, v in _FIELD_RE.findall(fields_blob)}
        # "page" is "v_branch/<hier>" / "v_toggle/<hier>" / etc. Strip the
        # hierarchy suffix; we already carry hierarchy separately via "h".
        page_raw = fields.get("page", "")
        page = page_raw.split("/", 1)[0]
        f    = fields.get("f", "?")
        l    = fields.get("l", "?")
        o    = fields.get("o", "?")
        h    = fields.get("h", "?")

        if page == "v_toggle":
            # Parse "name[bit]" → (name, bit). Aggregate rows ("clk") fall
            # back to bit=0 (the only bit of a 1-bit signal).
            m2 = _OBJ_BIT_RE.match(o)
            if m2:
                sig_raw, bit = m2.group(1), int(m2.group(2))
            else:
                sig_raw, bit = o, 0
            reg_fq = _fully_qualified(h, sig_raw, top_module)

            # Emit two point IDs per bit — :rise and :fall — matching the
            # xsim xtgl schema. Verilator's cov.dat doesn't split rise/fall
            # in the hit count, so both halves move in lockstep: either
            # both covered or both uncovered.
            for direction, val in (("rise", 1), ("fall", 0)):
                pid = f"xtgl:{h}:{sig_raw}:{bit}:{direction}"
                snap.universe.add(pid)
                snap.kinds[pid] = "toggle"
                snap.details[pid] = {
                    "page":   page, "file":   f,     "line":  l,
                    "object": o,    "hier":   h,
                    "reg":    reg_fq,
                    "bit":    bit,
                    "dir":    direction,
                }
                if hits > 0:
                    snap.points.add(pid)
                total += 1
            continue

        if page == "v_branch":
            pid  = f"branch:{f}:{l}:{o}"
            kind = "branch"
        elif page == "v_line":
            pid  = f"line:{f}:{l}:{o}"
            kind = "line"
        elif page == "v_user":
            pid  = f"user:{h}:{o}"
            kind = "user"
        else:
            pid  = f"{page or 'cov'}:{f}:{l}:{o}"
            kind = page or "unknown"

        snap.universe.add(pid)
        snap.kinds[pid] = kind
        snap.details[pid] = {
            "page":   page, "file":   f,     "line":  l,
            "object": o,    "hier":   h,
        }
        if hits > 0:
            snap.points.add(pid)
        total += 1

    snap.total = total or None
    return snap
